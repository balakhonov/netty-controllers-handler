package softserve.netty.server.mapping.request;

import io.netty.handler.mapping.PackageVersion;

public class UpdateUserRequestPackage extends PackageVersion {
	private int id;
	private String name;
	private int age;

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

}
